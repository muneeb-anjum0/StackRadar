import { AiReport } from "../../types/api";
import { SignalBadge } from "../../components/primitives/SignalBadge";

export function ReportReader({ report }: { report: AiReport | null }) {
  return (
    <div className="min-h-[520px] rounded-[1.5rem] border border-white/[0.1] bg-[#181818] p-7">
      {!report ? (
        <p className="text-sm text-neutral-400">Select a report to read it here.</p>
      ) : (
        <>
          <div className="flex flex-wrap gap-2">
            <SignalBadge tone={report.provider === "openrouter" ? "warn" : "neutral"}>Generated by: {report.provider}</SignalBadge>
            <SignalBadge>AI used: Yes</SignalBadge>
            <SignalBadge>Source data: StackRadar analytics</SignalBadge>
            <SignalBadge tone={report.reused_from_cache ? "good" : "neutral"}>Cached: {report.reused_from_cache ? "Yes" : "No"}</SignalBadge>
          </div>
          <h2 className="mt-6 text-3xl font-semibold capitalize text-neutral-100">{report.report_type.replace(/_/g, " ")}</h2>
          <p className="mt-2 text-sm text-neutral-500">{report.target_role} / {new Date(report.created_at).toLocaleString()} / model {report.model ?? "local"}</p>
          <pre className="mt-6 whitespace-pre-wrap text-sm leading-7 text-neutral-300">{report.output_text}</pre>
        </>
      )}
    </div>
  );
}
